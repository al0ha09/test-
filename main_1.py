# Вводим число
x = float(input("Введите число: "))

# Проверяем, что число положительное
if x <= 0:
    print("Число должно быть больше нуля.")
else:
    # Находим степень десяти, чтобы привести число к нужному виду
    import math

    # Определяем степень десяти
    b = int(math.floor(math.log10(x))) if x != 0 else 0
    # Преобразуем число в нужный формат
    a = x / (10 ** b)

    # Выводим результат в формате плавающей точки
    print(f"Формат плавающей точки: x = {a} * 10 ** {b}")
